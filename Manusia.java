public class Manusia {

    String mata = "Melihat";

    public void makan() {
        System.out.println("Manusia Makan");
    }

    public void minum() {
        System.out.println("Manusia Minum");
    }

    public void lari() {
        System.out.println("Manusia Lari");

    }

    public void melihat() {
        System.out.println("Manusia " + mata);
    }

 
}
